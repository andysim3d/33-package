  - 蜀势力技能〖志继〗：准备阶段或结束阶段，若你没有手牌，你回复1点体力或摸两张牌，减1点体力上限并获得技能〖观星〗。
  - 魏势力技能〖魏业〗：准备阶段，你可以弃置一张牌，令一名敌方角色选择一项：1.弃置一张牌；2.你摸一张牌。
  
  **攻其不备**：
  - 蜀势力技能〖连营〗：当你失去手牌后，若你没有手牌，你可以摸一张牌。
  - 魏势力技能〖突袭〗：摸牌阶段，你可以改为获得至多两名其他角色的各一张手牌。
  
